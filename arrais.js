// funciones 

const invertArray = (array) => {


   for (let aux = array.length- 1; aux >= 0; aux--) {
      console.log(array[aux])

   }

}

// funciones 
invertArray([1, 2, 3, 4, 5, 6, 7, 8, 9])


const derechoArray = (arreglo) => {

   for(let index = 0 ; index < arreglo.length ; index ++) {
      console.log(arreglo[index])

   }

}

derechoArray([1, 2, 3, 4, 5, 6, 7, 8, 9])